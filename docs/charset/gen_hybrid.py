def bitmap_to_svg_rects(bitmap, ox, oy, scale, color=None):
    """Convert 8x16 bitmap to SVG rect elements."""
    rects = []
    for row, val in enumerate(bitmap):
        for col in range(8):
            if val & (0x80 >> col):
                x = ox + col * scale
                y = oy + row * scale
                if color:
                    rects.append(f'<rect x="{x}" y="{y}" width="{scale}" height="{scale}" fill="{color}"/>')
                else:
                    rects.append(f'<rect x="{x}" y="{y}" width="{scale}" height="{scale}"/>')
    return rects


def render_char(char, font, ox, oy, scale, color=None):
    """Render a single character, return SVG rects."""
    if char in font:
        return bitmap_to_svg_rects(font[char], ox, oy, scale, color)
    return []


def render_text(text, font, ox, oy, scale, color=None):
    """Render a string of text, return SVG rects."""
    rects = []
    cw = 8 * scale
    for i, ch in enumerate(text):
        rects.extend(render_char(ch, font, ox + i * cw, oy, scale, color))
    return rects


