def build_n8_font():
    """Assemble all 256 characters into a single font array."""
    font = [None] * 256

    # Row $0x: Custom symbols
    for code, bitmap in N8_ROW_0X.items():
        font[code] = bitmap

    # Row $1x: More arrows/symbols
    for code, bitmap in N8_ROW_1X.items():
        font[code] = bitmap

    # ASCII $20-$7E: From PCFace
    for code, bitmap in PCFACE_ASCII.items():
        font[code] = bitmap

    # $7F: Custom reversed not sign
    for code, bitmap in N8_7F.items():
        font[code] = bitmap

    # Row $8x: Block elements
    for code, bitmap in N8_ROW_8X.items():
        font[code] = bitmap

    # Row $9x: More blocks
    for code, bitmap in N8_ROW_9X.items():
        font[code] = bitmap

    # Box drawing $A0-$BD
    for code, bitmap in N8_BOX_DRAWING.items():
        font[code] = bitmap

    # International $BE-$CF
    for code, bitmap in N8_INTERNATIONAL.items():
        font[code] = bitmap

    # Geometric $D0-$DF
    for code, bitmap in N8_GEOMETRIC.items():
        font[code] = bitmap

    # Math $E0-$EF
    for code, bitmap in N8_MATH.items():
        font[code] = bitmap

    # Misc $F0-$FF
    for code, bitmap in N8_MISC.items():
        font[code] = bitmap

    # Verify all 256 are defined
    missing = [i for i in range(256) if font[i] is None]
    if missing:
        raise ValueError(f"Missing character definitions: {[hex(i) for i in missing]}")

    return font


