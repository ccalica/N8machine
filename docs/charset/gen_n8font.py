def main():
    """Generate all output files."""
    print("Building N8 font...")
    font = build_n8_font()
    print(f"  {len(font)} characters defined")

    out_dir = os.path.dirname(os.path.abspath(__file__))

    # C header
    generate_c_header(font, os.path.join(out_dir, 'n8_font.h'))

    # SVG previews (all 3 colors)
    for color in ['white', 'green', 'amber']:
        generate_svg_preview(font, os.path.join(out_dir, f'n8_font_{color}.svg'), color)

    # Python dict
    generate_python_dict(font, os.path.join(out_dir, 'n8_font.py'))

    print("\nDone! Generated:")
    print("  - n8_font.h (C header)")
    print("  - n8_font_white.svg, n8_font_green.svg, n8_font_amber.svg (previews)")
    print("  - n8_font.py (Python dict)")


if __name__ == '__main__':
    main()
