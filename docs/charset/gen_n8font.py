# OUTPUT: C HEADER
# =========================================================================

def generate_c_header(font, filename):
    """Generate C header file with font array."""
    lines = [
        "/*",
        " * N8 Font - 8x16 Hybrid Aesthetic",
        f" * Generated: {datetime.now().isoformat()}",
        " * ",
        " * 256 characters, 16 bytes each (one byte per row, MSB = left pixel)",
        " * License: CC0 Public Domain (ASCII based on PCFace Modern DOS)",
        " */",
        "",
        "#ifndef N8_FONT_H",
        "#define N8_FONT_H",
        "",
        "#include <stdint.h>",
        "",
        "static const uint8_t n8_font[256][16] = {",
    ]

    for code in range(256):
        bitmap = font[code]
        hex_str = ', '.join(f'0x{b:02x}' for b in bitmap)
        comment = _get_char_name(code)
        lines.append(f"    {{ {hex_str} }}, // ${code:02X} {comment}")

    lines.append("};")
    lines.append("")
    lines.append("#endif /* N8_FONT_H */")

    with open(filename, 'w') as f:
        f.write('\n'.join(lines))
    print(f"Generated {filename}")


