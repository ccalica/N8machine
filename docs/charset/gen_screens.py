def bitmap_to_rects(bitmap, cx, cy, scale):
    """Convert an 8x16 bitmap to SVG rect elements.

    bitmap: list of 16 ints (each 8 bits)
    cx, cy: character position in screen pixels
    scale: pixel scale factor
    """
    rects = []
    for row_idx, row_val in enumerate(bitmap):
        for col_idx in range(8):
            if row_val & (0x80 >> col_idx):
                x = cx + col_idx * scale
                y = cy + row_idx * scale
                rects.append(f'<rect x="{x}" y="{y}" width="{scale}" height="{scale}"/>')
    return rects


def render_text_line(font, text, start_x, start_y, scale, col_start=0):
    """Render a line of text, returning list of SVG rect strings."""
    rects = []
    for i, ch in enumerate(text):
        glyph = font.get(ch)
        if glyph is None:
            continue  # skip unknown chars
        cx = start_x + (col_start + i) * 8 * scale
        cy = start_y
        rects.extend(bitmap_to_rects(glyph, cx, cy, scale))
    return rects


def build_screen_font(direction):
    """Build complete font dict for a direction."""
    font = make_font_common()
    blocks = make_blocks()
    arrows = make_arrows()
    syms = make_misc_symbols()

    if direction == 'A':
        box = make_box_rounded()
    elif direction == 'B':
        box = make_box_sharp()
    else:
        box = make_box_drawing()

    # Map special chars to font dict using placeholder chars
    # We'll use a mapping approach for the screen content
    font.update(blocks)
    font.update(box)
    font.update(arrows)
    font.update(syms)

    return font


def generate_screen_svg(direction, filename, title, subtitle, bg_color, fg_color, content_lines, scale=2, header_height=40):
    """Generate an 80x25 screen mockup SVG.

    content_lines: list of (text, y_row) or list of strings (one per row).
    Each string can contain regular chars and special tokens like {BOX_TL} etc.
    """
    font = build_screen_font(direction)

    char_w = 8 * scale  # pixels per char width
    char_h = 16 * scale  # pixels per char height

    # Determine screen size from content
    max_cols = 80
    max_rows = 25

    svg_w = max_cols * char_w + 20  # 10px margin each side
    svg_h = header_height + max_rows * char_h + 20

    rects = []

    for row_idx, line in enumerate(content_lines):
        if row_idx >= max_rows:
            break
        col = 0
        i = 0
        while i < len(line) and col < max_cols:
            if line[i] == '{':
                # Try to parse as token like {BOX_TL}
                close = line.find('}', i)
                if close != -1 and close - i < 20:
                    token = line[i+1:close]
                    glyph = font.get(token)
                    if glyph:
                        cx = 10 + col * char_w
                        cy = header_height + row_idx * char_h
                        rects.extend(bitmap_to_rects(glyph, cx, cy, scale))
                    col += 1
                    i = close + 1
                else:
                    # Literal '{' character
                    col += 1
                    i += 1
            elif line[i] == '}':
                # Literal '}' character
                col += 1
                i += 1
            else:
                ch = line[i]
                glyph = font.get(ch)
                if glyph:
                    cx = 10 + col * char_w
                    cy = header_height + row_idx * char_h
                    rects.extend(bitmap_to_rects(glyph, cx, cy, scale))
                col += 1
                i += 1

    # Assemble SVG
    svg_parts = [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{svg_w}" height="{svg_h}" viewBox="0 0 {svg_w} {svg_h}">',
        f'  <title>{title}</title>',
        f'  <rect width="{svg_w}" height="{svg_h}" fill="{bg_color}"/>',
        f'  <text x="10" y="20" fill="{fg_color}" font-family="monospace" font-size="14">{title}</text>',
        f'  <text x="10" y="34" fill="{fg_color}" font-family="monospace" font-size="10" opacity="0.6">{subtitle}</text>',
        f'  <g fill="{fg_color}">',
    ]
    svg_parts.extend('    ' + r for r in rects)
    svg_parts.append('  </g>')
    svg_parts.append('</svg>')

    with open(filename, 'w') as f:
        f.write('\n'.join(svg_parts))
    print(f"Generated {filename} ({len(rects)} rects)")


